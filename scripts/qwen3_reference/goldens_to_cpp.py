#!/usr/bin/env python3
"""Write test/qwen/goldens/*.inl from golden tensor dicts."""

from __future__ import annotations

from pathlib import Path

import torch


def key_to_c(name: str) -> str:
    parts = name.replace("L0", "L0_").split("_")
    out = "k"
    for p in parts:
        if p:
            out += p[0].upper() + p[1:]
    return out


def fmt_float(v: float) -> str:
    s = f"{v:.9g}"
    if "e" in s or "E" in s or "." in s:
        return s + "f"
    return s + ".0f"


def write_inc(path: Path, entries: dict[str, torch.Tensor], *, namespace: str = "module_golden_L0_pos0") -> None:
    chunks = [
        "// Generated by scripts/qwen3_reference/generate_goldens.py — do not edit by hand.",
        f"namespace {namespace} {{",
        "",
    ]

    for name, tensor in entries.items():
        vals = tensor.detach().float().reshape(-1).tolist()
        cname = key_to_c(name)
        chunks.append(f"inline constexpr size_t {cname}Size = {len(vals)};")
        chunks.append(f"inline constexpr float {cname}[{len(vals)}] = {{")
        for j in range(0, len(vals), 8):
            row = vals[j : j + 8]
            chunks.append("    " + ", ".join(fmt_float(v) for v in row) + ",")
        chunks.append("};")
        chunks.append("")

    chunks.append(f"}} // namespace {namespace}")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(chunks))
